# Tear down running network
function networkDown() {
  # stop gc containers also in addition to LocalNGO1 and LocalNGO2, in case we were running sample to add gc
  # docker-compose -f $COMPOSE_FILE -f $COMPOSE_FILE_COUCH -f $COMPOSE_FILE_gc down --volumes --remove-orphans
  docker-compose -f $COMPOSE_FILE -f $COMPOSE_FILE_COUCH down --volumes --remove-orphans

  # Don't remove the generated artifacts -- note, the ledgers are always removed
  if [ "$MODE" != "restart" ]; then
    # Bring down the network, deleting the volumes
    #Delete any ledger backups
    docker run -v $PWD:/tmp/first-network --rm hyperledger/fabric-tools:$IMAGETAG rm -Rf /tmp/first-network/ledgers-backup
    #Cleanup the chaincode containers
    clearContainers
    #Cleanup images
    removeUnwantedImages
    # remove orderer block and other channel configuration transactions and certs
    #rm -rf channel-artifacts/*.block channel-artifacts/*.tx crypto-config ./gc-artifacts/crypto-config/ channel-artifacts/gc.json
    # remove the docker-compose yaml file that was customized to the goodchar
    rm -f docker-compose-e2e.yaml
  fi
}
